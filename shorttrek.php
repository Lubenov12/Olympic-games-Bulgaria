      <p>Шорттрекът е зимен олимпийски спорт.</p>
      <p>Провежда се на 111-метрова кръгла писта (игрище за хокей на лед). За по-голяма стабилност при завоите състезателят се опира с ръка на леда и така запазва равновесие при високата скорост. Интересна част от състезанието са различните тактики, които се прилагат от спортистите. Борбата за първото място налага различни стратегии – забавяне, забързване, изпреварване. Победителите се определят след поредица от състезателни серии, в които само класиралите се на челните места продължават в надпреварата.</p>
