      <h1>Евгения Раданова</h1>
      <p>Евгения Раданова е българска спортистка и състезателка по шорттрек.</p>
      <p>Раданова е родена на 4 ноември 1977 г. в София. Висока е 166 см. Завършва НСА. Тя е първият български спортист, участвал на зимни и летни олимпийски игри (в дисциплината колоездене на писта в Атина 2004). В края на ноември 2001 г. Жени и нейна съотборничка падат заедно на леда по време на тренировка. Лицето и е срязано дълбоко на две места от кънките. Въпреки тежката физическа и психологическа травма, Раданова успява бързо и със завидна воля да се справи с последиците, завръщайки се отново в челните редици на международния шорттрек. През 2010 г. слага край на своята невероятна спортна кариера. Евгения Раданова е считана за една от най-великите спортистки на България. Доказва го със спечелването на 3 олимпийски медала от бели олимпиади, от които 2 от една, непостигано от български спортист.</p>
