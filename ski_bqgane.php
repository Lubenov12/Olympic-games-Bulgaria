      <h1>Иван Лебанов</h1>
      <p>Иван Лебанов е български ски бегач и първият българин медалист от зимна олимпиада.</p>
      <p>На 10 години участва в първото си състезание по ски бягане. На 17 години печели първата си републиканска титла, през 1974 и 1976 г. печели титли от балканските първенства за юноши. През 1977 г. става световен юношески шампион на 10 км ски бягане в „Сент-Кроа“, Швейцария. Печели състезанията на 15 км ски бягане от Универсиадите през 1978 и 1981 г. </p>
      	<P>През сезон 1978-1979 г. завършва в първите десет в три старта за световната купа и завършва девети в генералното класиране за сезона.
      През 1980 г. печели бронзов медал на зимните олимпийски игри в Лейк Плесид на 30 км ски бягане. Заема и 15-о място на 15 км на същата Олимпиада.
